public class StarPatternP5 {
    public static void main (String[]args){
        for(int i=1;i<=5;i++){
            for(int j =4;j<=i;j++){
                System.out.print("");
            }
            for(int h =1;h<=i;h++){
                System.out.print("P ");
            }
           System.out.println();
        }
    }
}
